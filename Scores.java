public class Scores{
	public static void main (String... args){
		int score = 100;
	if (score >= 1 && score <= 50)
		System.out.println("1 - 50");
	else if (score  <= 60)
		System.out.println("51 - 60");
	else if (score  <= 70)
		System.out.println("61 - 70");
	else if (score  <= 80)
		System.out.println("71 - 80");
	else if (score  <= 90)
		System.out.println("81 - 90");
	else if (score  <= 100)
		System.out.println("91 - 100");


	}

}
public class Astericks2{
	public static void main(String[] args){


		for (int count = 1; count <= 10; count++){

			for(int counter = 1; counter <= count; counter++){
				System.out.print("*");
			}
			System.out.println();

		}
			System.out.println();

		for (int count = 0; count <= 10; count++){

			for(int counter = 10; counter > count ; counter--){
				System.out.print("*");
			}
			System.out.println();

		}








	}		
}
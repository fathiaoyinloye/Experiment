public class Astericks3{
	public static void main(String[] args){

	for(int count = 1;  count <= 5; count++){
		for(int counter = 1; counter <= count; counter++){
			System.out.print("  ");
		}

		for(int counter = 5; counter >= count; counter--){
			System.out.print("* ");
		}

			System.out.println();


	}






	}
}